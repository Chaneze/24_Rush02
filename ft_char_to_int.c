#include "ft_colle2.h"

int		ft_char_to_int(char *str)
{
	int i;
	int rslt;

	i = 0;
	rslt = 0;
	while (str[i] != '\0' && str[i] >= '0' && str[i] <= '9')
	{
		rslt = rslt * 10 + str[i] - '0';
		i++;
	}
	return (rslt);
}
